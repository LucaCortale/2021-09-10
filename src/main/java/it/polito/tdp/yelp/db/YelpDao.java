	public List<Adiacenze> getAdiacenze(Map<String,Business> idMap,String c){
		
		String sql = "SELECT b1.business_id AS id1,b2.business_id AS id2, b1.city "+
				"FROM business b1,business b2 "+
				"WHERE b1.city = ? AND b2.city = ? AND b1.business_id > b2.business_id ";

		List<Adiacenze> result = new ArrayList<Adiacenze>();
		Connection conn = DBConnect.getConnection();

		try {
			PreparedStatement st = conn.prepareStatement(sql);
			st.setString(1, c);
			st.setString(2, c);
			ResultSet res = st.executeQuery();
			while (res.next()) {

				Adiacenze a = new Adiacenze(idMap.get(res.getString("id1")),
							idMap.get(res.getString("id2")),
							res.getString("city"));
						
				result.add(a);
			}
			res.close();
			st.close();
			conn.close();
			return result;
			
		} catch (SQLException e) {
			e.printStackTrace();
			return null;
		}
	}
	
	
